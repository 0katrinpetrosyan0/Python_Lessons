# 33. Ունենք ցուցակներով ցուցակ, ամեն ցուցակի մեջ ստանալ փոքր ցուցակներ, որտեղ ցուցակի ամեն անդամ
# ներկայացվում է այդ ցուցակի վերջին տարրի հետ։
# Ունենք ։ [[10, 11, 12], [5, 6, 8], [9, 2, 13]]
# Արդյունք : [[[10, 12], [11, 12]], [[5, 8], [6, 8]], [[9, 13], [2, 13]]]

test_list = [[10, 11, 12], [5, 6, 8], [9, 2, 13]]
res = []
for sub in test_list:
	res.append([[ele, sub[-1]] for ele in sub[:-1]])
print (str(res))
